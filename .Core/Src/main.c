   HAL_RTC_SetDate(&hrtc, &rtcDate, RTC_FORMAT_BIN );


   /* Time format: hh:mm:ss
    * eg 11:28:01
    *
    * with:  hh: two numbers for hours
    *        mm: two numbers for minutes
    *        ss: two numbers for seconds
    */
